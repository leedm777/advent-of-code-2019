(defn plot-line
  [y line]
  (->> line
       (map-indexed (fn [x ch]
                      (if (s/includes? "#^v<>" (str ch))
                        [[x y] ch])))
       (mapcat identity)))

(defn plot-map
  [screen]
  (->> screen
       (map-indexed plot-line)
       (mapcat identity)
       (filter some?)
       (apply hash-map)))

(defn find-intersections
  [plot]
  (->> plot
       (filter (fn [[pos ch]]
                 (->> dirs
                      (mapv #(pos-move pos %))
                      (every? #(contains? plot %)))))))

(defn sum-alignment-params
  [intersections]
  (->> intersections
       (map first)
       (map (fn [[x y]] (* x y)))
       (apply +)))

(defn init-robot
  [program]
  (let [brain (int-code program)
        [screen-ascii brain] (int-read-all-output brain)
        screen (s/split-lines (s/join (map char screen-ascii)))]
    {:brain brain
     :screen screen
     :map (plot-map screen)}))

(defn program-robot
  [robot movement]
  (let [brain (:brain robot)
        brain (int-resume-input brain movement)
        [output brain] (int-read-all-output brain)
        [screen-ascii total-dust] (partition-by #(< % 128) output)
        screen (s/split-lines (s/join (map char screen-ascii)))]
    (merge robot {:brain brain
                  :screen screen
                  :total-dust total-dust})))

(def movement-program
  (map int
       (s/join "\n"
               ;; 20 character limit per line
               ["A,B,C" ;; 10 instructions
                ;12345678901234567890
                "R,12,L,6,R,12,L,8"
                "L,6,L,10,R,12,L,6"
                "R,12,R,12,L,10,L,6,R"
                "10,L,8,L,6,L,10,"
                "n"
                ""])))

(defn solve
